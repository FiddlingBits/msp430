#include <stdint.h>
#include <string.h>

/****************************************************************************************************
 * Constants and Variables
 ****************************************************************************************************/

uint8_t hwMemmap_memory[65536];

/****************************************************************************************************
 * Function Definitions (Public)
 ****************************************************************************************************/

/****************************************************************************************************
 * FUNCT:   hwMemmap_init
 * BRIEF:   Initialize
 * RETURN:  Returns Nothing
 * ARG:     No Arguments
 ****************************************************************************************************/
void hwMemmap_init(void)
{
    memset(hwMemmap_memory, 0, sizeof(hwMemmap_memory));
}
